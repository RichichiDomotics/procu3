package mx.gob.segob.nsjp.dto.audiencia;

import mx.gob.segob.nsjp.dto.base.GenericDTO;

@SuppressWarnings("serial")
public class SituacionImputadoDTO extends GenericDTO{
	
	private Long situacionImputadoId;
    private String descripcion;
	private Long prioridad;

    // Constructors
    /** default constructor */
	public SituacionImputadoDTO() {
	}
	/** minimal constructor */

	public SituacionImputadoDTO(Long situacionImputadoId, Long prioridad,
			String descripcion) {
		super();
		this.situacionImputadoId = situacionImputadoId;
		this.prioridad = prioridad;
		this.descripcion = descripcion;
	}
	
	
	public Long getSituacionImputadoId() {
		return situacionImputadoId;
	}
	public void setSituacionImputadoId(Long situacionImputadoId) {
		this.situacionImputadoId = situacionImputadoId;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public Long getPrioridad() {
		return prioridad;
	}
	public void setPrioridad(Long prioridad) {
		this.prioridad = prioridad;
	}
}
